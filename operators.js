// Arithmetic Operators
let sum = 10 + 5; // 15
let product = 10 * 5; // 50

// Comparison Operators
let isEqual = 10 == 10; // true
let isIdentical = 10 === "10"; // false

// Logical Operators
let andOperation = true && false; // false
let orOperation = true || false; // true

console.log(sum, product, isEqual, isIdentical, andOperation, orOperation);